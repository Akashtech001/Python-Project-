num1 = 47
num2 = 102
sum = num1+num2
diff = num2-num1
print(f"{num1} + {num2} = {sum}")
print(f"{num2} - {num1} = {diff}")
print(f"{sum} * {diff} = {sum*diff}")
print(f"( {num1} + {num2} ) * ( {num2} - {num1} ) = {(num1+num2) * (num2-num1)}")
print(f"Dividing number {num1} with number 2 gives remainder {num1 % 2}")
print(f"Dividing number {num2} with number 2 gives remainder {num2 % 2}")
